		<h2>Persona: Justin Fisher</h2>
		<p><strong>Name: </strong>Justin Fisher</p>
		<p><strong>Age: </strong>26</p>
		<p><strong>Profession: </strong> Justin has been working as a front desk agent at the Hyatt Regency hotel for
			the past 3 and a half years. He has an Associates Degree in Hospitality and Tourism from CNM, but is not
			happy with his profession. Three and a half years at the hotel and he is still in the same position as when
			he first started. He is looking into a career change and has always been interested in technology and coding.
			Justin found the Deep Dive Coding Bootcamp's website while browsing the web for different technology fields he
			could go into. He decided to sign up for the bootcamp and is at the end of week 0.</p>
		<p><strong>Technology: </strong>Justin has a 12" Dell Inspiron Notebook that is about two years old and runs
			Windows 10. He also has a Samsung Galaxy S7 with the latest version of Android. He is fairly competent on
			both devices and is often the go to guy for friends and family that have questions about their device.</p>
		<p><strong>Attitudes &amp; Behaviors: </strong>Justin is the type of student that values interaction. Justin
			spends a lot of time studying and looking over material because he knows that learning doesn't necessarily
			come easy to him. He knows that he needs to study more than everyone else in order to be successful. While
			working on his prework for the bootcamp, Justin relied heavily on Youtube to get a better understanding of
			the new concepts he was learning. Justin would search through multiple videos, and leave comments asking
			questions, but most of the videos where dated one or more years back, therefore not closely monitored
			anymore. Because of this, he never received any replies to his questions. Justin wished he could receive
			extra help from the bootcamp, either from instructors or former students. He believes they would
			be the most helpful since they have gone through the class already, and are familiar with the curriculum.</p>
		<p><strong>Frustrations &amp; Needs: </strong>Justin has at times doubted his ability to be successful
			in the bootcamp due to the fact that he always needed more help learning than his classmates did all
			throughout school. When he is faced with new, complex material, he feels frustrated that he doesn't
			understand it as well as his classmates do. When this happens, Justin will ask the instructor after
			class to go over a concept again. This does help him, but he still wishes he had more time to spend with
			the instructor to go over it. This feeling sometimes makes it hard for him to continue and he has at times,
			thought about quitting. Although after his initial frustrations fade, he knows that he doesn't want to quit
			and that he just needs extra help learning the material. Justin has used tutors in the past, and thinks that a
			tutor would be just what he needs to help him grasp the material. However, he is concerned that he will
			not be able to find one that is qualified to take on the material he is learning from the bootcamp. This is
			when Justin again turns to Youtube and faces the same problems he had during the prework phase. Justin
			acknowledges the fact that he needs extra help. He wishes he could sit down with someone more experienced
			that is willing to take the time necessary to help him learn the material.</p>
		<p><strong>Goals: </strong>Justin's main goal is that he wants to become a developer. He knows that he is
			going to need outside help in the form of a tutor in order to maximize his learning potential. He is very
			focused on this goal and is actively seeking a tutor for assistance. Justin wants a tutor he can see
			regularly, that is familiar with the bootcamp curriculum. Ideally he wants someone that has gone through the
			bootcamp and has been successful in it. He thinks this is best because they would be familiar with the
			curriculum and have gone though the same thing that Justin is going through now.</p>
