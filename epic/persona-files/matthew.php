		<h1>Persona for Matthew Bretton, 32</h1>
		<h2>Role:</h2>
		<p>Matthew graduated from the Deep Dive Coding Bootcamp in early 2016 and found a job within a month doing back end, PHP and MySQL database work for an Albuquerque marketing company. He enjoys his job and gets paid well.</p>
		<h2>Technology:</h2>
		<p>Matthew still has the HP laptop that he bought for the coding bootcamp but he upgraded to Windows 10, and dual booted Linux. Even though he spends the workday on a laptop or desktop, Matthew prefers the mobility of his Samsung Galaxy S9 when not at work.</p>
		<h2>Attitude and Behavior:</h2>
		<p>Matthew enjoyed learning to code and worked hard to get the most out of the coding bootcamp. Even with this hard work, Matthew experienced some difficulty with the material in the bootcamp. Fortunately Matthew was able to work through those challenges. He feels that he has a little extra insight into the difficulties that a student may have when learning full stack web development.</p>
		<h2>Frustrations and Needs:</h2>
		<p>Although Matthew is making good money at his job, his school loan payments are high. He would like to find a way to make some extra money using his skills. Matthew would also like to help others that might be experiencing the same difficulties that he had.</p>
		<h2>Goals:</h2>
		<p>Matthew wants to find a tutoring gig that matches his education and experience.</p>
		<h2>Use Case:</h2>
		<p>Matthew stopped by the DDCB to say hello to Dylan and the crew and learned of the greatest website in existence, deepdivetutor.com. Later that day Matthew went to deepdivetutor.com on his android phone. He’s curious to see what tutoring demand there may be and how many tutors are offering their services.</p>
		<p>Luckily for Matthew the site was designed with the mobile first methodology and was easy for him to navigate on his phone. Not only was it easy for him to search for students that needed help with PHP and MySQL (what he worked with everyday), but it was also easy to see how he measured against the other tutors.</p>
		<br/>
		<p>Matthew sees clear info about how the site works and how it can help him right on the main page. He can see that the site connects students with tutors.</p>
		<p>Matthew then clicks the link to sign up as a tutor. He then enters the needed profile info, name, username, email, areas of specialty, education/deep dive program, availability, requested rate of pay, and uploaded a profile photo.</p>
		<br/>
		<p>Matthew knew that it may be harder to get his first client because he didn’t have a rating on the site yet.</p>
		<p>Within 48 hours Matthew received an email from a student, via deepdivetutor.com, that needed help with object oriented PHP and set his first tutoring appointment. The email included contact info for the student. Matthew emailed (or maybe called on the phone) the student directly and setup the first appointment to tutor them.</p>
		<p>The first tutoring session went well so Matthew asked the student to leave positive feedback for him on the website.</p>
		<br/>
		<h2>Interaction Flow 1</h2>
		<ol>
			<li>The perspective Matthew navigates to deepdiveMatthew.com</li>
			<li>The Matthew sees images and reads copy on the site that motivates him/her to register and offer their services as a tutor.</li>
			<li>The Matthew clicks the link to register as a tutor.</li>
			<li>The Matthew enters the needed profile info, name, username, email, topics of specialty, education/deep dive program, availability, requested rate of pay, and uploads a profile photo.</li>
			<li>The site will scan the profile info for inappropriate content and give the Matthew the opportunity to edit their entries.</li>
			<li>The Matthew's tutor profile will then be displayed for students to see.</li>
			<li>When a student sends a message to the Matthew, the site sends Matthew an email that includes the message, the topic(s) that the student needs help with, availability times and the student's contact info.</li>
			<li>Matthew will then contact the student and arrange a tutoringing appointment.</li>
		</ol>
		<h2>Interaction Flow 2</h2>
		<ol>
			<li>The Matthew has learned a new skill and needs to add it to his/her profile.</li>
			<li>The Matthew goes to deepdiveMatthew.com and logs in to his profile.</li>
			<li>Matthew clicks the "edit profile" link and is taken to the "edit profile" page.</li>
			<li>The "edit profile" page is the same as the registration page except the fields are populated with Matthew's existing profile info from the database.</li>
			<li>Matthew edits the info and submits the changes.</li>
			<li>All is right in the world</li>
		</ol>
